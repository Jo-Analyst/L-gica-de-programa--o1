// Objetivo: praticar variáveis e soma de inteiros.
// Conceitos: int, atribuição, operador +, print e interpolação ($).

void main() {
  // Declara duas variáveis inteiras (int).
  int valor1 = 15;
  int valor2 = 45;

  // Soma os dois valores e guarda o resultado.
  int resultado = valor1 + valor2;

  // Imprime a conta usando interpolação de strings ($variavel).
  print('$valor1 + $valor2 = $resultado');
}
